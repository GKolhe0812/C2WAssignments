class NestedFor14{

        public static void main(String[] args){

                int N = 4,num = 14;

                for(int i = 1;i<=N;i++){

                        for(int j = 1;j<=N;j++){

                                System.out.print(num + " ");

                        }
                        System.out.println();
			num++;
                }

        }
}
